function updateSchedule(scheduleData) {
  let indexSchedule = schedulesDB.findIndex(
    (schedule) => schedule.id === scheduleData.idSchedule,
  );

  //delete Schedule
  if (scheduleData.employees === undefined) {
    let employee = Object.assign({}, schedulesDB[indexSchedule].employees[0]);
    employee.fullname = scheduleData.employee.fullname;
    schedulesDB[indexSchedule].employees.push(employee);
  }

  //update employee
  if (scheduleData.employees.length === undefined) {
    let indexEmployee = schedulesDB[indexSchedule].employees.findIndex(
      (employee) => employee.id === scheduleData.employees.where.id,
    );
    schedulesDB[indexSchedule].employees[indexEmployee].fullname =
      scheduleData.employees.data.fullname;
  }

  //add employee
  if (scheduleData.employees.length > 0) {
    let indexEmployee = schedulesDB[indexSchedule].employees.findIndex(
      (employee) => employee.id === scheduleData.employees[0].where.id,
    );
    let indexDay = schedulesDB[indexSchedule].employees[
      indexEmployee
    ].daysOfEmployee.findIndex(
      (day) =>
        day.id ===
        scheduleData.employees[0].data.daysOfEmployee.update[0].where.id,
    );
    schedulesDB[indexSchedule].employees[indexEmployee].daysOfEmployee[
      indexDay
    ].kind =
      scheduleData.employees[0].data.daysOfEmployee.update[0].data.kind.update;
  }

  return {
    id: scheduleData.idSchedule,
    __typename: 'Schedule',
  };
}

